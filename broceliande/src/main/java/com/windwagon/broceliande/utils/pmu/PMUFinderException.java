package com.windwagon.broceliande.utils.pmu;

public class PMUFinderException extends Exception {

	private static final long serialVersionUID = 1L;

	public PMUFinderException() {
		super();
	}

	public PMUFinderException(String message) {
		super(message);
	}

	public PMUFinderException(String message, Throwable cause) {
		super(message, cause);
	}

	public PMUFinderException(Throwable cause) {
		super(cause);
	}

}
